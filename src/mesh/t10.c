void t10_get_ref_phi(gdn_real x, gdn_real y, gdn_real z, gdn_real *phi)
{
	phi[0] = (2 * z - 1 + 2 * y + 2 * x) * (z - 1 + y + x);
	phi[1] = x * (2 * x - 1);
	phi[2] = y * (2 * y - 1);
	phi[3] = z * (2 * z - 1);
	phi[4] = -4 * x * (z - 1 + y + x);
	phi[5] = 4 * x * y;
	phi[6] = -4 * y * (z - 1 + y + x);
	phi[7] = -4 * z * (z - 1 + y + x);
	phi[8] = 4 * x * z;
	phi[9] = 4 * y * z;
}

void t10_get_ref_grad_phi(gdn_real x, gdn_real y, gdn_real z,
						  gdn_real *grad_phi)
{
	grad_phi[0] = 4 * x + 4 * y + 4 * z - 3;
	grad_phi[1] = 4 * x - 1;
	grad_phi[2] = 0;
	grad_phi[3] = 0;
	grad_phi[4] = -4 * z + 4 - 4 * y - 8 * x;
	grad_phi[5] = 4 * y;
	grad_phi[6] = -4 * y;
	grad_phi[7] = -4 * z;
	grad_phi[8] = 4 * z;
	grad_phi[9] = 0;
	grad_phi[10] = 4 * x + 4 * y + 4 * z - 3;
	grad_phi[11] = 0;
	grad_phi[12] = 4 * y - 1;
	grad_phi[13] = 0;
	grad_phi[14] = -4 * x;
	grad_phi[15] = 4 * x;
	grad_phi[16] = -4 * z + 4 - 8 * y - 4 * x;
	grad_phi[17] = -4 * z;
	grad_phi[18] = 0;
	grad_phi[19] = 4 * z;
	grad_phi[20] = 4 * x + 4 * y + 4 * z - 3;
	grad_phi[21] = 0;
	grad_phi[22] = 0;
	grad_phi[23] = 4 * z - 1;
	grad_phi[24] = -4 * x;
	grad_phi[25] = 0;
	grad_phi[26] = -4 * y;
	grad_phi[27] = -8 * z + 4 - 4 * y - 4 * x;
	grad_phi[28] = 4 * x;
	grad_phi[29] = 4 * y;
}

